# 1) Escreva um programa que leia o comprimento (cm), a largura (cm) e a altura (cm) de uma caixa retangular e calcule o seu volume (cm3), cuja fórmula é: Volume = Comprimento * Largura * Altura.
# Teste o seu programa com as medidas abaixo:

comprimento = float(input("Informe o comprimento: "))

largura = float(input("Informe a largura: "))

altura = float(input("Informe a altura: "))

volume = comprimento * largura * altura

print("Volume: ", volume)