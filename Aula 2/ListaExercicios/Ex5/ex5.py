# 5) Leia dois valores A e B e efetue a troca do conteúdo das duas variáveis lidas,
# de forma que a variável A passe a ter o valor de B e a variável B o valor de A.
# Mostre o conteúdo das variáveis antes e depois da troca.

a = 10
b = 20

print("=== Antes da Troca ===")
print(f"A = {a}")
print(f"B = {b}")

aux = a
a = b
b = aux

print("=== Depois da Troca ===")
print(f"A = {a}")
print(f"B = {b}")




