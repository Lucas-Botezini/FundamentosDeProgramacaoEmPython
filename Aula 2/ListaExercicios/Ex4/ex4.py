n1 = float(input(f"Digite o primeiro numero: "))
n2 = float(input(f"Digite o segundo numero: "))

# Adicao
calc = n1 + n2
print("\n%d + %d = %d", n1, n2, calc)

# Subtracao
calc = n1 - n2
print("\n%d - %d = %d", n1, n2, calc)

# Multiplicao
calc = n1 * n2
print("\n%d * %d = %d", n1, n2, calc)

# Divisao
calc = n1 / n2
print("\n%d / %d = %d", n1, n2, calc)

# Divisao
calc = n1 / n2
print("\n%d / %d = %.2f", n1, n2, calc)

# Resto
calc = n1 % n2
print("\n%d %% %d = %d", n1, n2, calc)


